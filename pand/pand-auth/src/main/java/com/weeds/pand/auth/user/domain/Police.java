package com.weeds.pand.auth.user.domain;

public class Police {
    private Integer id;

    private String policeCode;

    private String policeName;

    private String policeNamePinyin;

    private String pPoliceCode;

    private String remark;

    private String areaCode;

    private Integer sort;

    private Integer flag;

    private String pointX;

    private String pointY;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPoliceCode() {
        return policeCode;
    }

    public void setPoliceCode(String policeCode) {
        this.policeCode = policeCode;
    }

    public String getPoliceName() {
        return policeName;
    }

    public void setPoliceName(String policeName) {
        this.policeName = policeName;
    }

    public String getPoliceNamePinyin() {
        return policeNamePinyin;
    }

    public void setPoliceNamePinyin(String policeNamePinyin) {
        this.policeNamePinyin = policeNamePinyin;
    }

    public String getpPoliceCode() {
        return pPoliceCode;
    }

    public void setpPoliceCode(String pPoliceCode) {
        this.pPoliceCode = pPoliceCode;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public Integer getSort() {
        return sort;
    }

    public void setSort(Integer sort) {
        this.sort = sort;
    }

    public Integer getFlag() {
        return flag;
    }

    public void setFlag(Integer flag) {
        this.flag = flag;
    }

    public String getPointX() {
        return pointX;
    }

    public void setPointX(String pointX) {
        this.pointX = pointX;
    }

    public String getPointY() {
        return pointY;
    }

    public void setPointY(String pointY) {
        this.pointY = pointY;
    }
}